type ColorTokensRecord = Record<
  string,
  {
    value: string
  }
>

type ColorTokensRecordWithVariants = Record<
  string,
  Record<
    string,
    {
      value: string
    }
  >
>

const colorsRecord = colors as ColorTokensRecordWithVariants | ColorTokensRecord

// add variants with opacity from 10% to 90% for every color:
Object.keys(colorsRecord).forEach(color => {
  if (typeof colorsRecord[color].value === 'string') {
    return
  }
  const colorVariants = colorsRecord[color] as ColorTokensRecordWithVariants[string]
  Object.keys(colorVariants).forEach(variant => {
    const newVariants: ColorTokensRecordWithVariants[string] = {}
    const value = colorVariants[variant].value
    newVariants[`${variant}-5`] = {
      value: `color-mix(in lch, ${value} 5%, transparent)`,
    }
    for (let i = 10; i <= 90; i += 10) {
      newVariants[`${variant}-${i}`] = {
        value: `color-mix(in lch, ${value} ${i}%, transparent)`,
      }
    }
    newVariants[`${variant}-95`] = {
      value: `color-mix(in lch, ${value} 95%, transparent)`,
    }
    colors[color] = {
      ...colors[color],
      ...newVariants,
    }
  })
})

console.log('> tokens.colors: ', Object.keys(colors))

